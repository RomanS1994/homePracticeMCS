import time
from scipy.optimize import linprog

start = time.time()

# Цільова функція для максимізації прибутку
# Множимо на -1, оскільки linprog мінімізує функцію
c = [
    -2,  # Вартість послуги для дизайнерів (Сайти)
    -9,  # Вартість послуги для розробників (Інтернет-магазини)
    -6,  # Вартість послуги для тестувальників (Інтеграція з ERP)
]

# Система обмежень
# Кількість годин для кожного спеціаліста не може перевищувати їх ліміти
A_ub = [
    [12, 12, 12],  # Години для дизайнерів, розробників, тестувальників (Сайти)
    [6, 24, 18],   # Години для дизайнерів, розробників, тестувальників (Інтернет-магазини)
    [2, 18, 12],   # Години для дизайнерів, розробників, тестувальників (Інтеграція з ERP)
]

# Ліміти по годинниках для кожної категорії
b_ub = [160, 160, 192, 180]  # Для дизайнерів, розробників і тестувальників

# Виконання оптимізації
result = linprog(c, A_ub=A_ub, b_ub=b_ub)

# Виведення результатів
print("Оптимальний розподіл:", result.x)
print("Максимальний дохід:", -result.fun)

stop = time.time()
print("Час обчислень:", stop - start)
